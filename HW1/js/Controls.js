var controls;
var controlsmusic;

var esc;

BasicGame.Controls.prototype = {

    create: function () {

        //  Honestly, just about anything could go here. It's YOUR game after all. Eat your heart out!
        this.controlsmusic = this.game.add.audio('controlsmusic');
    		this.controlsmusic.play();
        this.controlsmusic.volume = 0.4;

        this.controls = this.game.add.sprite(this.game.world.centerX, this.game.world.centerY, 'controls');
        this.controls.anchor.setTo(0.5, 0.5);

        this.esc = this.game.input.keyboard.addKey(Phaser.Keyboard.ESC);

    },

    update: function () {

        //  Honestly, just about anything could go here. It's YOUR game after all. Eat your heart out!

        // Accelerate the 'logo' sprite towards the cursor,
        // accelerating at 500 pixels/second and moving no faster than 500 pixels/second
        // in X or Y.
        // This function returns the rotation angle that makes it visually match its
        // new trajectory.

        if(this.esc.isDown)
        {
          this.loadMenu();
        }
    },

    loadMenu: function () {

        //  Here you should destroy anything you no longer need.
        //  Stop music, delete sprites, purge caches, free resources, all that good stuff.

        //  Then let's go back to the main menu.
        this.controlsmusic.stop();
        this.state.start('MainMenu');

    }

};
